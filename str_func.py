def word_upp (value: str) -> str:
    """Функция, которая принимает на вход строку и возвращает ее сделав все буквы большими"""
    return value.upper()


def word_title (value: str) -> str:
    """Функция, которая принимает на вход строку и возвращает ее сделав первые буквы большими"""
    return value.title()


